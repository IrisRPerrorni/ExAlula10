    //Faça um programa que mostra na tela os números de 100 a 1.

    public static void main(String[] args){

        System.out.println("Liste os números de 100 a 1: ");

        int i = 100 ;
        do{
            System.out.println(i);
            i--;

        } while(i>0);

    }

